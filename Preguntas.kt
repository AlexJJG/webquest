Investigue los siguientes términos:

Push: La traducción literal sería: rama. Es decir, dentro de nuestro sistema de control de versiones podemos ver el histórico de
cambios como si de un árbol se tratase. 

Commit: La operación commit indica la finalización de una transacción satisfactoria. Indica al administrador de transacciones que una unidad de trabajo lógica ha concluido satisfactoriamente, que la base de datos está o debería estar nuevamente en un estado consistente.

DELETE: La instrucción DELETE permite eliminar uno o múltiples registros. Incluso todos los registros de una tabla, dejándola vacía.

Stack: Una pila (stack en inglés) es una lista ordenada o estructura de datos que permite almacenar y recuperar datos, el modo de acceso a sus elementos es de tipo LIFO (del inglés Last In, First Out, «último en entrar, primero en salir») 

Repositorio: Repositorio es un término que tiene su raíz etimológica en repositorium, un vocablo latino. Un repositorio es un espacio que se utiliza para almacenar distintas cosas. 